# Tipe Data String dalam Python

# Membuat string
nama = "Maya"
kalimat = "Semangat Belajar Semangat Cumlaude!"

# Menampilkan string
print(nama)
print(kalimat)

# Menggabungkan string
gabungan = nama + " senang belajar Python"
print(gabungan)

# Mengambil sebagian string
print(kalimat[0:8])   # mengambil karakter 0 sampai 6

# Mengetahui panjang string
print("Panjang kalimat:", len(kalimat))
