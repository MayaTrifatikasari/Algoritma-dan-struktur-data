# Tipe Data Integer dalam Python

# Membuat variabel integer
umur = 19
tahun_lahir = 2006
tanggal_lahir = 16
bulan_lahir = 5

# Menampilkan nilai
print(umur)
print(tahun_lahir)
print(tanggal_lahir)
print(bulan_lahir)

# Operasi sederhana dengan integer
total = umur + tanggal_lahir
print("Total:", total)

# Mengecek tipe data
print(type(umur))
