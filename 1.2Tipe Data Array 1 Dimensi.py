# Tipe Data Array Satu Dimensi

# array satu dimensi
nama_mahasiswa = ["maya", "indri","dewy","ana"]

# Menampilkan isi array
print("nama_mahasiswa:", nama_mahasiswa)

# Mengakses elemen array
print("nama mahasiswa pertama:", nama_mahasiswa[0])
print("nama mahasiswa ketiga :", nama_mahasiswa[2])

# Mengubah nilai elemen
nama_mahasiswa[1] = "marna"
print("nama setelah diubah:", nama_mahasiswa)

# Menambahkan elemen baru
nama_mahasiswa.append("nur")
print("nama setelah ditambah:", nama_mahasiswa)
  